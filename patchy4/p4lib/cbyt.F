      SUBROUTINE CBYT (M,LM,IX,LX,NB)
C
C CERN PROGLIB# M421    CBYT            .VERSION KERNFOR  4.23  891215
C ORIG. 14/08/70 CL
C
      PARAMETER   (NBITPW=32)
      PARAMETER   (IALL11 = -1)
C
      IX = OR ( AND (IX,
     +                  NOT(ISHFT (ISHFT(IALL11,-NBITPW+NB), LX-1)))
