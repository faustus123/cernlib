     +             C5(:L5),C6(:L6),C7(:L7))
      CALL FMEMCPY(K1,C1(:L1))
      CALL FMEMCPY(K2,C2(:L2))
      CALL FMEMCPY(K3,C3(:L3))
      CALL FMEMCPY(K4,C4(:L4))
      CALL FMEMCPY(K5,C5(:L5))
