      SUBROUTINE K77CHECK(F77,C,L,N)
      CHARACTER*(*) F77,C
      IF(L.GT.LEN(C)) THEN
         PRINT '(A,I1,3A,I4)'
     +      ,' *** argument C',N,' in ',F77
     +      ,' has to be increased to CHARACTER*',L
      ENDIF
      END
*
      FUNCTION K77C(KF77,K1,L1)
      EXTERNAL KF77
      CHARACTER*1024 C1
      CALL K77CHECK('K77C',C1,L1,1)
      CALL UHTOC(K1,4,C1,L1)
      K77C = KF77(C1(:L1))
      CALL FMEMCPY(K1,C1(:L1))
      END
*
