#if !defined(CERNLIB_DOUBLE)
      ENTRY RCOSIN(X)
#endif
#if defined(CERNLIB_DOUBLE)
      ENTRY DCOSIN(X)
#endif

      IF(X .EQ. 0) THEN
       H=0
       CALL MTLPRT(NAME,'C336.1','ARGUMENT X = 0')
      ELSEIF(ABS(X) .LE. 8) THEN
       H=R32*X**2-1
       ALFA=H+H
       B1=0
       B2=0
       DO 4 I = 15,0,-1
       B0=C(I)+ALFA*B1-B2
       B2=B1
    4  B1=B0
       H=CE+LOG(ABS(X))-B0+H*B2
      ELSE
       R=1/X
       H=128*R**2-1
       ALFA=H+H
       B1=0
       B2=0
       DO 5 I = 28,0,-1
       B0=P(I)+ALFA*B1-B2
       B2=B1
    5  B1=B0
       PP=B0-H*B2
       B1=0
       B2=0
       DO 6 I = 24,0,-1
       B0=Q(I)+ALFA*B1-B2
       B2=B1
    6  B1=B0
       H=R*((B0-H*B2)*SIN(X)-R*PP*COS(X))
      END IF
#if !defined(CERNLIB_DOUBLE)
    9 RSININ=H
#endif
#if defined(CERNLIB_DOUBLE)
    9 DSININ=H
#endif
