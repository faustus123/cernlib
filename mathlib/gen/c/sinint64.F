      IF(ABS(X) .LE. 8) THEN
       Y=R8*X
       H=2*Y**2-1
       ALFA=H+H
       B1=0
       B2=0
       DO 1 I = 15,0,-1
       B0=S(I)+ALFA*B1-B2
       B2=B1
    1  B1=B0
       H=Y*(B0-B2)
      ELSE
       R=1/X
       H=128*R**2-1
       ALFA=H+H
       B1=0
       B2=0
       DO 2 I = 28,0,-1
       B0=P(I)+ALFA*B1-B2
       B2=B1
    2  B1=B0
       PP=B0-H*B2
       B1=0
       B2=0
       DO 3 I = 24,0,-1
       B0=Q(I)+ALFA*B1-B2
       B2=B1
    3  B1=B0
       H=SIGN(PIH,X)-R*(R*PP*SIN(X)+(B0-H*B2)*COS(X))
      END IF
