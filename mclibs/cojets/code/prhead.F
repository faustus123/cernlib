#include "cojets/pilot.h"
      SUBROUTINE PRHEAD
C     *****************
C-- PRINTS HEADING
#if defined(CERNLIB_SINGLE)
      IMPLICIT REAL (A-H,O-Z)
#endif
#if defined(CERNLIB_DOUBLE)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
#endif
#include "cojets/alqgen.inc"
#include "cojets/cuejet.inc"
#include "cojets/cuevol.inc"
#include "cojets/data3.inc"
#include "cojets/davers.inc"
#include "cojets/event.inc"
#include "cojets/evtype.inc"
#include "cojets/fstate.inc"
#include "cojets/idrun.inc"
#include "cojets/intype.inc"
#include "cojets/itapes.inc"
#include "cojets/jet.inc"
#include "cojets/jetset.inc"
#include "cojets/keybre.inc"
#include "cojets/khadro.inc"
#include "cojets/maxn.inc"
#include "cojets/nflav.inc"
#include "cojets/over.inc"
#include "cojets/par.inc"
#include "cojets/parq.inc"
#include "cojets/parqua.inc"
#include "cojets/spyfor.inc"
#include "cojets/tleave.inc"
C
      CHARACTER*6 CHFLA(3)
      CHARACTER*8 LABEL
C
      DATA CHFLA/'CHARM','BOTTOM','TOP'/
      WRITE(ITLIS,1)
      WRITE(ITLIS,2)
      WRITE(ITLIS,26)
      WRITE(ITLIS,5)
      WRITE(ITLIS,11) FLOAT(IDVER)/100.,DAVERS
      WRITE(ITLIS,2)
      WRITE(ITLIS,6) IDG
      WRITE(ITLIS,2)
      WRITE(ITLIS,3)
      WRITE(ITLIS,3)
      IF(IPBAR.EQ.1) WRITE(ITLIS,18) ECM
      IF(IPBAR.NE.1) WRITE(ITLIS,19) ECM
      WRITE(ITLIS,3)
      IF(IEVTYP.EQ.1) CALL HEADH
      IF(IEVTYP.GE.2.AND.IEVTYP.LE.4) CALL HEADB
      IF(KHADRO.EQ.0) WRITE(ITLIS,40)
      IF(SPYC.GT.0.) WRITE(ITLIS,44)
      IF(SPYB.GT.0.) WRITE(ITLIS,45)
      IF(SPYT.GT.0.) WRITE(ITLIS,46)
1     FORMAT(1H1,5(/))
2     FORMAT(T2,120(1H*)/T2,120(1H*))
3     FORMAT(T2,1H*,T121,1H*)
4     FORMAT(T2,120(1H*),5(/))
5     FORMAT(T2,120(1H*))
26    FORMAT(T2,54(1H*),12X,54(1H*)/T2,54(1H*),12H   COJETS   ,54(1H*)
     *   /T2,54(1H*),12X,54(1H*))
11    FORMAT(T2,46(1H*),' VERSION',F6.2,' -- ',A9,' ',46(1H*))
6     FORMAT(T2,53(1H*),'    RUN ID    ',53(1H*)
     *      /T2,53(1H*),'  Y M D H M S ',53(1H*)
     *      /T2,53(1H*),' ', 2I6,' ',53(1H*))
18    FORMAT(T2,1H*,10X,28HANTIPROTON-PROTON COLLISIONS,10X,5HECM =,
     1E10.3,4H GEV ,T121,1H*/T2,1H*,T121,1H*)
19    FORMAT(T2,1H*,10X,24HPROTON-PROTON COLLISIONS,10X,5HECM =,E10.3
     1     ,4H GEV ,T121,1H*/T2,1H*,T121,1H*)
40    FORMAT(T2,1H*,3X,44HREQUESTED FREEZE OF FINAL STATE AT THE LEVEL,
     *10H OF QUANTA     ,T121,1H*/T2,1H*,T121,1H*)
44    FORMAT(T2,1H*,3X,43HREQUESTED FREEZE OF CHARM PARTICLE DECAYS  ,
     *  T121,1H*/T2,1H*,T121,1H*)
45    FORMAT(T2,1H*,3X,43HREQUESTED FREEZE OF BOTTOM PARTICLE DECAYS  ,
     *  T121,1H*/T2,1H*,T121,1H*)
46    FORMAT(T2,1H*,3X,39HREQUESTED FREEZE OF TOP PARTICLE DECAYS  ,
     *  T121,1H*/T2,1H*,T121,1H*)
C
      IF(FRGTHO) WRITE(ITLIS,81)
81    FORMAT(T2,1H*,3X,'JET FRAGMENTATION A LA BIDDULPH AND'
     *,' THOMPSON REQUESTED'
     *   ,T121,1H*/T2,1H*,T121,1H*)
C
      IF(LFORSL.NE.0) WRITE(ITLIS,56) CHFLA(KFORSL-3)
     *  ,CHA2(INTID(LFORSL)),CHA2(INTID(-LFORSL)),NFORSL
56    FORMAT(T2,1H*,3X,'FORCED DECAY OF ',A6,' PARTICLES INTO '
     *,A4,'/',A4,' SEMILEPTONICALLY -- ',I2,' TIMES'
     *   ,T121,1H*/T2,1H*,T121,1H*)
C
      IF(NFLAV.GE.6.AND.KYTQED.EQ.0) WRITE(ITLIS,67)
      IF(NFLAV.GE.6.AND.KYTQED.EQ.1) WRITE(ITLIS,68)
      IF(NFLAV.GE.6.AND.KYTQED.EQ.2) WRITE(ITLIS,69)
67    FORMAT(T2,1H*,3X,'QED RAD. EFFECTS IN TOP SEMILEPTONIC DECAYS'
     *,' ARE NEGLECTED'
     *,T121,1H*/T2,1H*,T121,1H*)
68    FORMAT(T2,1H*,3X,'QED RAD. EFFECTS IN TOP SEMILEPTONIC DECAYS'
     *,' ARE CALCULATED AT O(ALPHA) + LPA'
     *,T121,1H*/T2,1H*,T121,1H*)
69    FORMAT(T2,1H*,3X,'QED RAD. EFFECTS IN TOP SEMILEPTONIC DECAYS'
     *,' ARE CALCULATED AT O(ALPHA)'
     *,T121,1H*/T2,1H*,T121,1H*)
C
      IF(KEYBRE.EQ.0) GO TO 66
      WRITE(ITLIS,3)
      WRITE(ITLIS,3)
      IF(INTYPE.EQ.0) WRITE(ITLIS,61) KEYBRE
61    FORMAT(T2,1H*,3X,45H****WARNING -- JOB HAS BEEN RUN WITH KEYBRE =
     1 ,I2,53H (PARTIAL OR TOTAL SUPPRESSION OF QCD BREMSSTRAHLUNG)
     2 ,4H**** ,
     *  T121,1H*/T2,1H*,T121,1H*)
      IF(INTYPE.EQ.1) WRITE(ITLIS,62) KEYBRE
62    FORMAT(T2,1H*,3X,45H****WARNING -- JOB HAS BEEN RUN WITH KYEVOL =
     1 ,I2,53H (PARTIAL OR TOTAL SUPPRESSION OF QCD BREMSSTRAHLUNG)
     *,T121,1H*/T2,1H*,T121,1H*)
66    CONTINUE
C
      IF(NFLAV.LT.6.OR.KYTQCD.EQ.0) GO TO 63
      WRITE(ITLIS,3)
      WRITE(ITLIS,3)
      WRITE(ITLIS,64) KYTQCD
64    FORMAT(T2,1H*,3X,45H****WARNING -- JOB HAS BEEN RUN WITH KYTQCD =
     1 ,I2,T121,1H*/T2,1H*,3X
     2,'****(PARTIAL OR TOTAL SUPPRESSION OF QCD BREMSSTRAHLUNG IN TOP'
     2,' DECAYS)'
     *,T121,1H*/T2,1H*,T121,1H*)
63    CONTINUE
C
      IF(NODECY) WRITE(ITLIS,72)
72    FORMAT(T2,1H*,3X,'****ALL PARTICLE DECAYS HAVE BEEN SUPPRESSED'
     *,T121,1H*/T2,1H*,T121,1H*)
C
      IF(NSTOPD.EQ.0) GO TO 76
      WRITE(ITLIS,73)
73    FORMAT(T2,1H*,3X,'DECAYS OF THE FOLLOWING PARTICLES HAVE BEEN'
     *,' SUPPRESSED:',T121,1H*)
      DO 74 N=1,NSTOPD
      IKSTOP=INTID(KSTOPD(N))
      WRITE(ITLIS,75) CHA2(IKSTOP),KSTOPD(N),IKSTOP
75    FORMAT(T2,1H*,3X,A8,'  IDENT = ',I6,'  (INTERNAL ID = ',I5,')'
     *,T121,1H*)
74    CONTINUE
      WRITE(ITLIS,3)
76    CONTINUE
C
      IF(NFORCE.EQ.0) GO TO 77
      WRITE(ITLIS,78)
78    FORMAT(T2,1H*,3X,'FORCED DECAY MODES:',T121,1H*)
      DO 79 N=1,NFORCE
      DO 85 L=1,6
      IF(KFORCE(L,N).EQ.0) GO TO 86
      M=L
85    CONTINUE
86    CONTINUE
      IF(M.EQ.2) WRITE(ITLIS,92) (LABEL(KFORCE(L,N)),KFORCE(L,N),L=1,M)
      IF(M.EQ.3) WRITE(ITLIS,93) (LABEL(KFORCE(L,N)),KFORCE(L,N),L=1,M)
      IF(M.EQ.4) WRITE(ITLIS,94) (LABEL(KFORCE(L,N)),KFORCE(L,N),L=1,M)
      IF(M.EQ.5) WRITE(ITLIS,95) (LABEL(KFORCE(L,N)),KFORCE(L,N),L=1,M)
      IF(M.EQ.6) WRITE(ITLIS,96) (LABEL(KFORCE(L,N)),KFORCE(L,N),L=1,M)
92    FORMAT(T2,1H*,3X,(2X,A5,' (',I5,')'),'  -->'
     *, (2X,A5,' (',I5,')')
     *,T121,1H*)
93    FORMAT(T2,1H*,3X,(2X,A5,' (',I5,')'),'  -->'
     *,2(2X,A5,' (',I5,')')
     *,T121,1H*)
94    FORMAT(T2,1H*,3X,(2X,A5,' (',I5,')'),'  -->'
     *,3(2X,A5,' (',I5,')')
     *,T121,1H*)
95    FORMAT(T2,1H*,3X,(2X,A5,' (',I5,')'),'  -->'
     *,4(2X,A5,' (',I5,')')
     *,T121,1H*)
96    FORMAT(T2,1H*,3X,(2X,A5,' (',I5,')'),'  -->'
     *,5(2X,A5,' (',I5,')')
     *,T121,1H*)
79    CONTINUE
      WRITE(ITLIS,3)
77    CONTINUE
C
      IF(NOUNST) WRITE(ITLIS,97)
97    FORMAT(T2,1H*,3X,'****STABLE PARTICLES ONLY HAVE BEEN RETAINED'
     *,T121,1H*/T2,1H*,T121,1H*)
      IF(.NOT.NOUNST) WRITE(ITLIS,98)
98    FORMAT(T2,1H*,3X,'BOTH STABLE AND UNSTABLE PARTICLES HAVE BEEN'
     *,' RETAINED'
     *,T121,1H*/T2,1H*,T121,1H*)
C
      IF(INTYPE.EQ.1.AND.(.NOT.NOUNST).AND.KTPFRM.EQ.0.AND.NFLAV.GE.6)
     * WRITE(ITLIS,21)
21    FORMAT(T2,1H*,3X,'TOP DECAY HISTORY INCLUDES PARTONS'
     *,T121,1H*/T2,1H*,T121,1H*)
      IF(INTYPE.EQ.1.AND.(.NOT.NOUNST).AND.KTPFRM.EQ.1.AND.NFLAV.GE.6)
     * WRITE(ITLIS,22)
22    FORMAT(T2,1H*,3X,'TOP DECAY HISTORY AS IN ISAJET (NO PARTONS)'
     *,T121,1H*/T2,1H*,T121,1H*)
C
      IF(NEVENT.GT.0) THEN
        CALL TIMELF(TLEFT)
        DTIME=(TINIT-TLEFT)/FLOAT(NEVENT)
        WRITE(ITLIS,3)
        WRITE(ITLIS,20) DTIME
      ENDIF
20    FORMAT(T2,1H*,3X,'TIME SPENT PER EVENT:',E12.3,' SEC'
     *,T121,1H*/T2,1H*,T121,1H*)
C
      WRITE(ITLIS,3)
      WRITE(ITLIS,3)
      WRITE(ITLIS,3)
      IF(SPYOVR.GT.0.) WRITE(ITLIS,49)
49    FORMAT(T2,1H*,48H****DIMENSION OVERFLOW HAS OCCURRED (NOT COUNTED,
     162H IN STATISTICS BELOW) -- LOOK FOR DIAGNOSTICS BEFORE THIS PRIN,
     28HTING****      ,T121,1H*/T2,1H*,T121,1H*)
C
      WRITE(ITLIS,50)
50    FORMAT(T2,1H*,1X,35H**UTILIZATION OF ARRAY DIMENSIONS**  ,T121,
     11H*/T2,1H*,1X,'MAX UTILIZATION OF VARIABLE LENGTH ARRAYS',
     2' IS REPORTED',T121,1H* ,
     3      /T2,1H*,1X,'THEIR DIMENSIONS CAN BE CHANGED BY MEANS OF',
     4' LISTED DIMENSION CONTROL PARAMETERS',T121,1H*)
      MNBOOK=MAX(MNBOOK,MMBOOK+1)
      WRITE(ITLIS,52) MXPART,MNPART,MAXQUA,MNQUA,MXJSET,MNJSET
     1,MAXJTP,MNJTP,MXINQ,MNBOOK,MXCUEV,MNCUEV,MXCUEJ,MNCUEJ
52    FORMAT(
     * T2,1H*,1X,'CURR. DIM. MXPART=',I6,'  USED UP TO',I6,T121,1H*
     */T2,1H*,1X,'CURR. DIM. MAXQUA=',I6,'  USED UP TO',I6,T121,1H*
     */T2,1H*,1X,'CURR. DIM. MXJSET=',I6,'  USED UP TO',I6,T121,1H*
     */T2,1H*,1X,'CURR. DIM. MAXJTP=',I6,'  USED UP TO',I6,T121,1H*
     */T2,1H*,1X,'CURR. DIM. MXINQ =',I6,'  USED UP TO',I6,T121,1H*
     */T2,1H*,1X,'CURR. DIM. MXCUEV=',I6,'  USED UP TO',I6,T121,1H*
     */T2,1H*,1X,'CURR. DIM. MXCUEJ=',I6,'  USED UP TO',I6,T121,1H*
     *)
C
      WRITE(ITLIS,3)
      WRITE(ITLIS,4)
C
      RETURN
      END
