#include "cojets/pilot.h"
      SUBROUTINE PARTAB
C     *****************
C-- PRINTS TABLE OF PARAMETERS FOR PARTICLES AND THEIR DECAYS
#if defined(CERNLIB_SINGLE)
      IMPLICIT REAL (A-H,O-Z)
#endif
#if defined(CERNLIB_DOUBLE)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
#endif
#include "cojets/data1.inc"
#include "cojets/data2.inc"
#include "cojets/data3.inc"
#include "cojets/decpar.inc"
#include "cojets/fstate.inc"
#include "cojets/itapes.inc"
#include "cojets/maxn.inc"
#include "cojets/mb.inc"
#include "cojets/par.inc"
#include "cojets/stable.inc"
      CHARACTER*8 LABEL
      DIMENSION IDFV(5)
C-- PARTICLE DATA HEADING
      WRITE(ITLIS,6)
6     FORMAT('1',' PARTICLE DATA'/' ',13('-')/' '
     1/6X,'IDENT',5X,'LABEL',13X,'MASS',4X,'CHARGE',T50,'INT. ID'/' ')
C-- QUARKS
      DO 1 IQ=1,6
      WRITE(ITLIS,2) IQ,CHA4(IQ,1),QMAS(IQ)
      WRITE(ITLIS,2) -IQ,CHA4(IQ,2),QMAS(IQ)
2     FORMAT(' ',I10,5X,A5,5X,E12.5)
1     CONTINUE
C-- GLUON ID
      WRITE(ITLIS,3) IDGL,0.
3     FORMAT(' ',I10,5X,'GL   ',5X,E12.5)
C-- LEPTONS ETC.
      DO 11 ID=10,100
      DO 12 LS=1,3,2
      IDS=(2-LS)*ID
      INTC=INTID(IDS)
      IF(INTC.GT.0)
     *WRITE(ITLIS,100) IDS,CHA2(INTC),PMAS(INTC),LCHARG(INTC),INTC
100   FORMAT(' ',I10,5X,A5,5X,E12.5,I10,T52,'(',I3,')')
12    CONTINUE
11    CONTINUE
C-- MESONS
      DO 21 LSP=1,2
      JSPIN=LSP-1
      DO 21 K=1,6
      DO 21 J=1,6
      IF(J.GT.K) GO TO 21
      DO 22 LS=1,3,2
      ID=(2-LS)*(100*J+10*K+JSPIN)
      INTC=INTID(ID)
      IF(INTC.GT.0)
     *WRITE(ITLIS,100) ID,CHA2(INTC),PMAS(INTC),LCHARG(INTC),INTC
22    CONTINUE
21    CONTINUE
C-- BARYONS
      DO 31 LSP=1,2
      JSPIN=LSP-1
      DO 31 K=1,6
      DO 31 J=1,K
      DO 31 I=1,J
      DO 33 LNV=1,2
      IP=I
      JP=J
      KP=K
      IF(LNV.EQ.2) THEN
        IF(JSPIN.EQ.1) GO TO 33
        IF(JSPIN.EQ.0.AND.(I.EQ.J.OR.J.EQ.K)) GO TO 33
        IP=J
        JP=I
      ENDIF
      DO 32 LS=1,3,2
      ID=(2-LS)*(1000*IP+100*JP+10*KP+JSPIN)
      INTC=INTID(ID)
32    IF(INTC.GT.0)
     *WRITE(ITLIS,100) ID,CHA2(INTC),PMAS(INTC),LCHARG(INTC),INTC
33    CONTINUE
31    CONTINUE
C-- WARNING IF DECAY TABLE HAS BEEN ALTERED BY STOP OR FORCE DECAY
C-- COMMANDS
      IFLAG=0
      DO 201 I=1,352
      IF(RDECAY(I).NE.0.) GO TO 201
      IFLAG=1
201   CONTINUE
      IF(IFLAG.EQ.0.AND.NFORCE.EQ.0.AND.NSTOPD.EQ.0) GO TO 202
      WRITE(ITLIS,203)
203   FORMAT(//' ***WARNING***'//' PRINTED DECAY TABLE IS AFFECTED'
     *,' BY FOLLOWING STOP AND FORCE DECAY COMMANDS:')
      IF(IFLAG.EQ.0) GO TO 204
      WRITE(ITLIS,205)
205   FORMAT(/' PARTICLES WHOSE DECAYS HAVE BEEN STOPPED:')
      DO 206 I=1,352
      IF(RDECAY(I).NE.0.) GO TO 206
      ID=IDEXT(I)
      WRITE(ITLIS,207) LABEL(ID),ID,I
207   FORMAT(1X,A5,5X,'ID =',I6,5X,'INT. ID =',I4)
206   CONTINUE
204   CONTINUE
      IF(NFORCE.EQ.0) GO TO 202
      WRITE(ITLIS,208)
208   FORMAT(//' DECAYS FORCED:')
      DO 209 I=1,NFORCE
      DO 211 L=1,6
211   IF(KFORCE(L,I).NE.0) LX=L
      WRITE(ITLIS,210) (LABEL(KFORCE(L,I)),KFORCE(L,I),L=1,LX)
210   FORMAT(' '/1X,(2X,A5,' (',I5,')'),'  -->',5(2X,A5,' (',I5,')'))
209   CONTINUE
202   CONTINUE
C-- DECAY DATA HEADING
      WRITE(ITLIS,7)
7     FORMAT(/' '/' '/' PARTICLE DECAYS'/' ',15('-')/' '
     1/8X,'DECAYING',2X,'CUMULATIVE',22X,'DECAY PRODUCTS'
     2/8X,'PARTICLE',8X,'B.R.'/' ')
C-- DECAY DATA
      ONE=1.
      EPSI=1.E-10
      LD=0
41    LD=LD+1
      IF(LD.GT.MNDEC) GO TO 44
      DO 42 LP=1,352
      IDFP=IDENTF(LP)
      LDI=ABS(IDB(LP))
      IF(LDI.EQ.LD.AND.IDFP.GT.0) GO TO 43
42    CONTINUE
      GO TO 41
43    CONTINUE
47    IF(LD.EQ.LDI) THEN
        IF(ABS(CBR(LD)).LT.EPSI) GO TO 48
      ELSE
        IF(ABS(CBR(LD)-CBR(LD-1)).LT.EPSI) GO TO 48
      ENDIF
      DO 45 KD=1,5
      IDFV(KD)=0
      KK=KDP(LD,KD)
      IF(KK.EQ.0) GO TO 49
      KDX=KD
      IDFV(KD)=IDEXT(KK)
45    CONTINUE
49    WRITE(ITLIS,46)
     *LABEL(IDFP),IDFP,CBR(LD),(LABEL(IDFV(KD)),IDFV(KD),KD=1,KDX)
46    FORMAT(1X,(2X,A5,' (',I5,')'),F12.5,3X,5(2X,A5,' (',I5,')'))
48    IF(ABS(CBR(LD)-ONE).LT.EPSI) GO TO 41
      LD=LD+1
      IF(LD.GT.MNDEC) GO TO 44
      GO TO 47
44    CONTINUE
C-- FRAGMENTATION PARAMETERS
      WRITE(ITLIS,101) CX2Q,CX2G,EPSPET,SIGMAQ,SIGMAG
101   FORMAT(5(/),1X,'MAIN FRAGMENTATION PARAMETERS'/1X,29(1H-)
     2//1X,9HCX2Q   = ,E12.4,'  IN FRAGMENTATION FUNCTION FOR LIGHT'
     3,' QUARK JETS'
     4/24X,'D(X) = (1-CX2Q+3*CX2Q*(1-X)**2)'
     2//1X,9HCX2G   = ,E12.4,'  IN FRAGMENTATION FUNCTION FOR GLUON'
     3,' JETS'
     4/24X,'D(X) = (1-CX2G+3*CX2G*(1-X)**2)'
     3//1X,12HEPSPET(1) = ,E12.4,8H (CHARM),5X,
     3     12HEPSPET(2) = ,E12.4,9H (BOTTOM),5X,
     3     12HEPSPET(3) = ,E12.4,6H (TOP)
     4     /1X,31HIN PETERSON ET AL. FRAGMENTATIO,
     527HN FUNCTION FOR HEAVY QUARKS/24X,25HD(X) = 1/(X*(1-1/X-EPSPET,
     610H/(1-X)**2)
     7//1X,9HSIGMAQ = ,E12.4,'  (GEV) -- QUARK PT GENERATION IN QUARK'
     8,' JETS'
     7//1X,9HSIGMAG = ,E12.4,'  (GEV) -- QUARK PT GENERATION IN GLUON'
     8,' JETS'
     9)
C
C-- HADRONIZATION PARAMETERS IN BEAM JETS
      WRITE(ITLIS,110) CLPRO,PT2PRO
110   FORMAT(5(/),1X,'BEAM JETS PARAMETERS'/1X,20(1H-)
     1  //1X,10HCLPRO   = ,E12.4,34H IN EXP(-CLPRO*X) -- LONGITUDINAL  ,
     2    32HDISTRIBUTION OF LEADING NUCLEONS
     3//1X,9HPT2PRO = ,E12.4,39H  GEV**2 MEAN PT**2 OF LEADING NUCLEONS
     3 )
C
      WRITE(ITLIS,111) FMULMB,FPTMB
111   FORMAT(///1X,'<N> AND <PT> OF BEAM JET HADRONS ',
     161HORIGINALLY SET UP TO REPRODUCE MINIMUM BIAS 1981 DATA OF UA1 ,
     37HAND UA5/1X,43HARE NOW MULTIPLIED BY THE FOLLOWING FACTORS  ,
     4    //1X,9HFMULMB = ,E12.4,26H FOR THE MEAN MULTIPLICITY
     5      //1X,9HFPTMB  = ,E12.4,16H FOR THE MEAN PT )
C
      WRITE(ITLIS,112) RETPIZ,RPAIR
112   FORMAT(////1X,50HPARTICLE RATIOS (PARTICLES ARE GENERATED IN PAIRS
     1 ,31H-- DATA ARE STORED IN RPAIR( ))
     2  /1X,56H(REF. K.ALPGARD ET AL. (UA5) PHYS.LETT. 121B (1983) 209)
     3   //1X,10HETA/PI0 = ,E12.4,9H (RETPIZ)
     4     //1X,20HPAIR OF (PI0 OR ETA),T30,E12.4
     5       /1X,12HPAIR PI-,PI+,T30,E12.4
     6         /1X,10HPAIR K+,K-,T30,E12.4
     7           /1X,11HPAIR K0,K0B,T30,E12.4
     8             /1X,9HPAIR P,PB,T30,E12.4
     9               /1X,9HPAIR N,NB,T30,E12.4
     1                 /1X,19HPAIR LAMBDA,LAMBDAB,T30,E12.4
     2                   /1X,19HPAIR SIGMA+,SIGMA+B,T30,E12.4
     3                     /1X,19HPAIR SIGMA-,SIGMA-B,T30,E12.4
     4                       /)
C
      RETURN
      END
