#include "cojets/pilot.h"
      SUBROUTINE TBOOK(IBOOK,IFLA1,TPSQ1,X1T,PX1T,PY1T,IBOOK1)
C     ********************************************************
C-- BOOKS TIMELIKE QUANTA GENERATED BY EVOL
#if defined(CERNLIB_SINGLE)
      IMPLICIT REAL (A-H,O-Z)
#endif
#if defined(CERNLIB_DOUBLE)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
#endif
#include "cojets/event.inc"
#include "cojets/itapes.inc"
#include "cojets/nevol.inc"
#include "cojets/parq.inc"
      MBK=MBOOK(IBEAM)
      MBK=MBK+1
      IF(MBK.GT.MXSPE) GO TO 500
      MBOOK(IBEAM)=MBK
      IBOOK1=MBK
      PARSPE(MBK,1,IBEAM)=PX1T
      PARSPE(MBK,2,IBEAM)=PY1T
      PARSPE(MBK,3,IBEAM)=X1T*ECM
      PARSPE(MBK,4,IBEAM)=IBOOK
      PARSPE(MBK,5,IBEAM)=-TPSQ1
      PARSPE(MBK,6,IBEAM)=IFLA1
      RETURN
C
C-- ABNORMAL EXIT
500   WRITE(ITLIS,501) MXINQ,NEVOL
501   FORMAT(5(/),1X,'NUMBER OF INITIAL STATE QUANTA EXCEEDS',I10
     1 //1X,'CASCADE NO. =',I10
     3 //1X,'INCREASE MXINQ'
     4 //1X,'EXECUTION TERMINATED'
     5 )
      CALL OVERDM
      RETURN
      END
