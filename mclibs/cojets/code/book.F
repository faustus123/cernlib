#include "cojets/pilot.h"
      SUBROUTINE BOOK(IFLA1,IBOOK,XM1,Y1,X1,PX1S,PY1S,YVI1,YVF1)
C     **********************************************************
C-- BOOKS SPACELIKE QUANTA GENERATED BY EVOL
#if defined(CERNLIB_SINGLE)
      IMPLICIT REAL (A-H,O-Z)
#endif
#if defined(CERNLIB_DOUBLE)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
#endif
#include "cojets/entrev.inc"
#include "cojets/itapes.inc"
#include "cojets/nevol.inc"
#include "cojets/parq.inc"
      NBK=NBOOK(IBEAM)
      IF(JBOOK.EQ.1)
     1NBK=NBK+1
      IF(NBK.GT.MXINQ) GO TO 500
      NBOOK(IBEAM)=NBK
      PARACT(NBK,1,IBEAM)=IFLA1
      PARACT(NBK,2,IBEAM)=IBOOK
      PARACT(NBK,3,IBEAM)=XM1
      PARACT(NBK,4,IBEAM)=Y1
      PARACT(NBK,5,IBEAM)=X1
      PARACT(NBK,6,IBEAM)=PX1S
      PARACT(NBK,7,IBEAM)=PY1S
      PARACT(NBK,8,IBEAM)=YVI1
      PARACT(NBK,9,IBEAM)=YVF1
      IBACK(IBEAM)=1
      RETURN
C
C-- ABNORMAL EXIT
500   WRITE(ITLIS,501) MXINQ,NEVOL
501   FORMAT(5(/),1X,'NUMBER OF INITIAL STATE QUANTA EXCEEDS',I10
     1 //1X,'CASCADE NO. =',I10
     3 //1X,'INCREASE MXINQ'
     4 //1X,'EXECUTION TERMINATED'
     5 )
      CALL OVERDM
      RETURN
      END
