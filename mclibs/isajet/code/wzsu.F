     1 64.*T3*WZM2-32.*T3*WM4-32.*S13*WZM2)
      WZSU=WZSU
     1 +CSXCU*CA3*U**2*(64.*U1*T3+64.*U1*S13)
     1 +EPF(P1,P2,P3,Q1)*CSXCU*CV3*(32.*ZM2*T1+32.*ZM2*U1+32.*WZM2)
