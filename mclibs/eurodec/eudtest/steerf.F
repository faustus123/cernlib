#include "eurodec/pilot.h"
      SUBROUTINE STEERF
C.----------------------------------------------------------------------
C.
C.    STEERING OF TEST PROGRAM FOR THE EURODEC PARTON FRAGMENTATION AND
C.    PARTICLE DECAYS. LOOP OVER PARTONS IN THE PARTICLE TABLE, FRAGMENT
C.    THEM AND LET FRAGMENTS DECAY (USES HBOOK LIBRARY ROUTINES!).
C.    LAST UPDATE: 01/03/89
C.
C.----------------------------------------------------------------------
C--
C--   BOOK HISTOGRAMS
      CALL HBOOK1(1,'UP QUARK FRAGMENTATION FUNCTION (F(Z))$',
     &20,0.,1.,0.)
      CALL HBOOK1(2,'DOWN QUARK FRAGMENTATION FUNCTION (F(Z))$',
     &20,0.,1.,0.)
      CALL HBOOK1(3,'STRANGE QUARK FRAGMENTATION FUNCTION (F(Z))$',
     &20,0.,1.,0.)
      CALL HBOOK1(4,'CHARM QUARK FRAGMENTATION FUNCTION (F(Z))$',
     &20,0.,1.,0.)
      CALL HBOOK1(5,'BOTTOM QUARK FRAGMENTATION FUNCTION (F(Z))$',
     &20,0.,1.,0.)
      CALL HBOOK1(6,'TOP QUARK FRAGMENTATION FUNCTION (F(Z))$',
     &20,0.,1.,0.)
      CALL HBOOK1(7,'LOW QUARK FRAGMENTATION FUNCTION (F(Z))$',
     &20,0.,1.,0.)
      CALL HBOOK1(8,'HIGH QUARK FRAGMENTATION FUNCTION (F(Z))$',
     &20,0.,1.,0.)
      CALL HBOOK1(9,'GLUON FRAGMENTATION FUNCTION (F(Z))$',
     &20,0.,1.,0.)
C--
C--   INITIALIZE FRAGMENTATION AND DECAY PACKAGE...
      CALL EUDINI
C--
C--   TEST FULL FRAGMENTATION AND DECAY
      CALL TESTF
C--
C--   WRITE HISTOGRAMS TO FILE ON LOGICAL UNIT LUN2
      CALL HISTDO
      END
