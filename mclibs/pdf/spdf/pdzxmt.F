      SQ = LOG ( LOG( MAX(Q0,QQ) /ALAM) / LOG(Q0/ALAM) )
      IF(IBLP .EQ. 6) THEN
        SQ = LOG ( LOG( MAX(Q0,QQ) /ALAM * (100./TM)) / LOG(Q0/ALAM))
      ENDIF
C
      FX(0) = EXP(D1)
      FX(1) = X
      FX(2) = 1.- X
      FX(3) = LOG (1.+ 1./X)

      PDFD = ONED
      DO 20 IEX = 0, MEX(IST)
        A(IEX) = AC(IEX, 0, IFL, IST)
        SQN=SQ
        DO 21 IPN = 1, MPN(IST)
          A(IEX) = A(IEX) + AC(IEX, IPN, IFL, IST) * SQN
          SQN    = SQN*SQ
21      CONTINUE
        FXIEX = FX(IEX)
        AIEX  = A(IEX)
        PDFD = PDFD * FXIEX **(AIEX)
   20 CONTINUE
C
C     PDZXMT = PDF / X
      PDF = PDFD
      IF(PDF.GT.0.) PDZXMT = PDF
C
      RETURN
C
      ENTRY VLAMBD (ISET, IORDER)
C
      IORDER = IORD (ISET)
      VLAMBD = ALM  (ISET)
C
      RETURN
