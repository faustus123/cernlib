     .     .5D0,.525D0,.55D0,.575D0,.6D0,.65D0,.7D0,.75D0,
     .     .8D0,.9D0,1.D0/
      DATA XMIN,XMAX,QSQMIN,QSQMAX/1.D-6,1.D0,5.D0,1310720.D0/
      DATA N0/4,5,8,8,8,8,0,0/
      DATA INIT/0/

      xsave=x

      IF(INIT.NE.0) GOTO 10
      INIT=1
      DO 20 N=1,nx-1
      DO 20 M=1,19
C     READ( 55,50)F(1,N,M),F(2,N,M),F(3,N,M),F(4,N,M),F(5,N,M),F(6,N,M),
C    .            F(7,N,M),F(8,N,M)
C 1=UV 2=DV 3=GLUE 4=UBAR 5=DBAR 6=SBAR 7=CBAR 8=BBAR
         DO 25 I=1,8
  25     F(I,N,M)=F(I,N,M)/(1.D0-XX(N))**N0(I)
  20  CONTINUE
      DO 30 J=1,ntenth-1
      XX(J)= LOG10(XX(J))+1.1D0
      DO 30 I=1,7
      DO 30 K=1,19
  30  F(I,J,K)= LOG(F(I,J,K))*F(I,ntenth,K)/ LOG(F(I,ntenth,K))
  50  FORMAT(8F10.5)
      DO 40 I=1,8
      DO 40 M=1,19
  40  F(I,nx,M)=0.D0
  10  CONTINUE
      IF(X.LT.XMIN) X=XMIN
      IF(X.GT.XMAX) X=XMAX
      QSQ=SCALE**2
      IF(QSQ.LT.QSQMIN) QSQ=QSQMIN
      IF(QSQ.GT.QSQMAX) QSQ=QSQMAX
      XXX=X
      IF(X.LT.1.D-1) XXX= LOG10(X)+1.1D0

      N=0
  70  N=N+1
      IF(XXX.GT.XX(N+1)) GOTO 70
      A=(XXX-XX(N))/(XX(N+1)-XX(N))
      RM= LOG(QSQ/QSQMIN)/ LOG(2.D0)
      B=RM-AINT(RM)
      M=1+  INT(RM)
      DO 60 I=1,8
      G(I)= (1.D0-A)*(1.D0-B)*F(I,N,M)+(1.D0-A)*B*F(I,N,M+1)
     .    + A*(1.D0-B)*F(I,N+1,M)  + A*B*F(I,N+1,M+1)
      IF(N.GE.ntenth) GOTO 65
      IF(I.EQ.8) GOTO 65
          FAC=(1.D0-B)*F(I,ntenth,M)+B*F(I,ntenth,M+1)
          G(I)=FAC**(G(I)/FAC)
  65  CONTINUE
      G(I)=G(I)*(1.D0-X)**N0(I)
  60  CONTINUE
      UPV=G(1)
      DNV=G(2)
      GLU=G(3)
      UBAR=G(4)
      DBAR=G(5)
      STR=G(6)
      CHM=G(7)
      BOT=G(8)

      x=xsave

      RETURN
      END
