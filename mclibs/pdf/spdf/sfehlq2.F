C...Determine Lamdba and x and t expansion variables.
        X = DX
        SCALE = DSCALE
        KF = ABS(NFL)
        Q2  =SCALE*SCALE
        TMIN=LOG(Q02/ALAM**2)
        TMAX=LOG(1E8/ALAM**2)
          T=LOG(MAX(1.,Q2/ALAM**2))
        VT=MAX(-1.,MIN(1.,(2.*T-TMAX-TMIN)/(TMAX-TMIN)))
        NX=1
        IF(X.LE.0.1) NX=2
