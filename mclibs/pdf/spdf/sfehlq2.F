C...Put into output variables
      DUP = XQ(1)
      DDN = XQ(2)
      DSEA = XQ(3)
      DGL  = XQ(4)
      DSTR = XQ(5)
      DCHM = XQ(6)
      DBOT = 0.D0
      DTOP = 0.D0
C
        IF (KF.GE.5) THEN
