      DATA ((Z(I,K),I=1,18),K=5,8) /
     +0.012332, -0.002796,  0.000000, -0.205990,  0.123876,  0.070738,
     +10.590572, 0.702136, -0.391293,  0.000000, 16.654520,  8.695223,
