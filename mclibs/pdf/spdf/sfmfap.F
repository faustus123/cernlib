      AL=0.231
      Q2=SCALE*SCALE
      Q0=2.
      ARG=( LOG(SCALE/AL)/ LOG(Q0/AL))
      S= LOG(ARG)

      DO 10 J=1,8

      A=Z(1,J)+Z(2,J)*S+Z(3,J)*S*S
      B=Z(4,J)+Z(5,J)*S+Z(6,J)*S*S
      C=Z(7,J)+Z(8,J)*S+Z(9,J)*S*S
      D=Z(10,J)+Z(11,J)*S+Z(12,J)*S*S
