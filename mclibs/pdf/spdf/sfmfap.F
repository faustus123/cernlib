   10 F(J)=A*X**B*(1.-X)**C*(1.+D*SQRT(X)+E*X)*( LOG(1./X))**H
      UPV=F(1)
      DNV=F(2)
      GLU=F(3)
      DUSUM=F(4)
      DUDIF=F(5)
      STR=F(6)
      CHM=F(7)
      BOT=F(8)
      DSEA=0.5D0*(DUSUM+DUDIF)
      USEA=0.5D0*(DUSUM-DUDIF)
      IF(Q2.LT.2.7D0) CHM=0.D0
      IF(Q2.LT.30.D0) BOT=0.D0
      IF(CHM.LT.0.D0) CHM=0.D0
      IF(BOT.LT.0.D0) BOT=0.D0
      RETURN
      END
