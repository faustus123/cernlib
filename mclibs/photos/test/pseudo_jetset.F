      COMMON/PHOLUN/PHLUN
      DO 10 I=1,5
   10 SUMVEC(I)=0.
C--
C--   Print event number...

      WRITE(PHLUN,9010) NEVHEP
      WRITE(PHLUN,9020) NHEP
