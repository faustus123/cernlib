        WRITE(PHLUN,9030) I,ISTHEP(I) 
        WRITE(PHLUN,9040) I,IDHEP(I)
        WRITE(PHLUN,9050) I,JMOHEP(1,I)  
        WRITE(PHLUN,9060) I,JMOHEP(2,I)  
        WRITE(PHLUN,9070) I,JDAHEP(1,I)
        WRITE(PHLUN,9080) I,JDAHEP(2,I)
        DO K=1,5
          WRITE(PHLUN,9090) K,I,PHEP(K,I)
        ENDDO
        DO K=1,4
          WRITE(PHLUN,9100) K,I,VHEP(K,I)
        ENDDO
      ENDDO
 9010 FORMAT(7X ,'NEVHEP=',I10)
 9020 FORMAT(7X ,'NHEP  =',I10)
 9030 FORMAT(7X ,'ISTHEP(  ',I3,')  =',I10)
 9040 FORMAT(7X ,'IDHEP(   ',I3,')  =',I10)
 9050 FORMAT(7X ,'JMOHEP(1,',I3,')  =',I10)
 9060 FORMAT(7X ,'JMOHEP(2,',I3,')  =',I10)
