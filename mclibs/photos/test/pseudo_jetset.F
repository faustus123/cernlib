 9080 FORMAT(7X ,'JDAHEP(2,',I3,')  =',I10)
 9090 FORMAT(7X ,'PHEP(',I3,',',I3,')  =',E16.9)
 9100 FORMAT(7X ,'VHEP(',I3,',',I3,')  =',E16.9)
      END
