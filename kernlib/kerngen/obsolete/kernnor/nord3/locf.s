       MODULE ADRVAR
%
% CERN PROGLIB# N100    LOCF            .VERSION KERNNOR  2.08  891120
% ORIG.  H.OVERAS, CERN, 791003
%
% LOCF   RETURNS ADDRESS OF VARIABLE OR DESCRIPTOR
% LOCSTR RETURNS ADDRESS OF CHARACTER STRING
%
       EXPORT LOCF,LOCB,LOCSTR
       ROUTINE LOCF
       LIB LOCF
LOCF: ENTF VBAS
       W1 LADDR IND(B.PAR)
       W SHL W1,-2
       RET
VBAS:  STACK FIXED
PAR:   W BLOCK 1
       ENDSTACK
       ENDROUTINE
       ROUTINE LOCB
       LIB LOCB
LOCB: ENTF VBAS
       W1 LADDR IND(B.PAR)
       RET
VBAS:  STACK FIXED
PAR:   W BLOCK 1
       ENDSTACK
       ENDROUTINE
       ROUTINE LOCSTR
       LIB LOCSTR
LOCSTR: ENTF VBAS
       W1:=1
       W1:=IND(B.PAR)(R1)
       RET
VBAS:  STACK FIXED
PAR:   W BLOCK 1
       ENDSTACK
       ENDROUTINE
       ENDMODULE
