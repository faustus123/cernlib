      CALL SIOUT (XNUM)
      GO TO 13
C
C
C     CALL TRANSLATE NAME/SYMBOL AND FIND IF NO ERRORS.
 16   CALL SISYMB
      IF(IND) GO TO 13
      GO TO 14
C
C     SPECIAL TREATEMENT OF FULLSTOP, IF IT NOT BELONGS TO THE NUMBER.
 17   IC=IC+1
      ISGN=SINSTR(IC)
      K=IC
  301 CONTINUE
      IF(SICOMP(KQBLAN)) GO TO 301
      IF(SIWHAT(0)) GO TO 19
      GOTO 14
 19   IC=K-1
C
C     NUMBER TRANSLATION CALL AND FIND IF ERROR FREE.
 20   CALL SINUMB
       IF (.NOT. (IND)) GO TO 14
      IF(MODE.EQ.1) GOTO 37
       CALL SIOUT(6)
       ICMM=ICMPI-2
       CALL SIOUT(ICMM)
       GOTO 13
  37  CONTINUE
      CALL SIOUT (2)
      CALL SIOUT (XNUM)
      GOTO 13
C
C     CHECK IF STRING OR SYMBOLSTRING FOR DISPLAY
C
  21  CONTINUE
      IF (CN .EQ. KQQUOT) GOTO 210
      GOTO 13
C
  210 CONTINUE
1239  CONTINUE
      ISGN=SINSTR(IC)
      IF (SICOMP(KQQUOT)) GO TO 1333
      IS22=IOC
      CALL SIOUT (0)
      CALL UCTOH(ISGN,ISI2,1,1)
      CALL SIOUT (ISI2)
      LL=1
 22   CONTINUE
      IF (.NOT.SICOMP(KQQUOT)) GO TO 23
*
* END OF STRING
*
      IS1=IOC
      IOC=IS22
      CALL SIOUT (LL)
      IOC=IS1
      GO TO 13
C
1333  CONTINUE
      PRINT *,'Null String'
      CALL SIOUT (1)
C      CALL SIOUT (ISGN)    // V.Fine 27.06.96
      CALL UCTOH(ISGN,ISI2,1,1)
      CALL SIOUT (ISI2)

      CALL SIOUT (1)
      goto 13
   23 CONTINUE
      IF (IC .GT. LENSTR) GOTO 14
C     ERROR IF CLOSING QUOTE MARK MISSING
      IC=IC+1
      ISGN=SINSTR(IC)
      CALL UCTOH(ISGN,ISI2,1,1)
      CALL SIOUT (ISI2)
      LL=LL+1
      GO TO 22
C     AND HERE IS END OF REAL SIINPS WORK.
 25   CALL SIOUT (5) 
      IND=.TRUE.
C     UNSAVE POINTERS OF COMPILEC
      IPOINT=NIPOIN
      IST=NIST
      ICSP=NICSP
      LDISP=NLDISP
      LNS=NLNS
      LSTAPO=NLSTAP
      KS=NKS
      IC=NIC
C
C
      IF (SITRAK(5).EQ.0) GO TO 27
      DO 26 I=1,IOC
      K=IOUTS(I)
      WRITE (NPRINT,38) K,K,X
 38   FORMAT (1X,'IOUTS()=',8X,I6,I6,8X,F16.8)
 26   CONTINUE
 27   RETURN
C
C
