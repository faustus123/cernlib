 33   CONTINUE
      LINE =  9
      IN=1
      DO 34 I=IN,10
      K=I+1
      KOLTEX(I)=KOLTEX(K)
 34   CONTINUE
      KOLTEX(10)='         '
      GO TO 2
C
      END
